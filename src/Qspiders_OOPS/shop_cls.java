package Qspiders_OOPS;

public class shop_cls {
    private String name;
    private String gst;
    private String address;
    private long mob;

    public void set_name(String name){
        this.name=name;
    }

    public String get_name(){
        return this.name;
    }

    public void set_gst(String gst){
        this.name=name;
    }

    public String get_gst(){
        return this.gst;
    }

    public void set_address(String address){
        this.address=address;
    }

    public String get_address(){
        return this.address;
    }

    public void set_mob(long mob){
        this.mob=mob;
    }

    public long get_mob(){
        return this.mob;
    }
}
